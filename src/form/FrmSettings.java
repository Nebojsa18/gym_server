    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        String url = txtUrl.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        try {
            validateTxt(url,username,password);
            Properties properties = new Properties();
            properties.load(new FileInputStream("dbconfig/config.properties"));
            properties.setProperty("url", txtUrl.getText().trim());
            properties.setProperty("username", txtUsername.getText().trim());
            properties.setProperty("password", String.copyValueOf(txtPassword.getPassword()));
            properties.store(new FileOutputStream("dbconfig/config.properties"), "url conficuration updated");
            this.dispose();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmSettings.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmSettings.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(FrmSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

