    private void validateTxt(String url, String username, String password) throws Exception {
        String errorMessage = "";
        jLabel1.setText("");
        jLabel5.setText("");
        jLabel6.setText("");
        if (url.isEmpty()) {
            errorMessage += "URL can not be empty!\n";
            jLabel1.setText("Username can not be empty!\n");
        }
        if (username.isEmpty()) {
            errorMessage += "Username can not be empty!\n";
            jLabel5.setText("Username can not be empty!\n");
        }
        if (password.isEmpty()) {
            errorMessage += "Password can not be empty!\n";
            jLabel6.setText("Password can not be empty!\n");
        }
        if (!errorMessage.isEmpty()) {
            throw new Exception("You must fill username and password");
        }
    }

    private void populateForm() {
        try {
            Properties properties = new Properties();
            properties.load(new FileInputStream("config/dbconfig.properties"));
            txtUrl.setText(properties.getProperty("url"));
            txtUsername.setText(properties.getProperty("username"));
            txtPassword.setText(properties.getProperty("password"));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmSettings.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmSettings.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void prepareForm() {
        txtPassword.setEnabled(false);
        txtUrl.setEnabled(false);
        txtUsername.setEnabled(false);
        jButton1.setEnabled(false);
    }
